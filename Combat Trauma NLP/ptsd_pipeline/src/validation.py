"""
validation.py
=============
Stage 3.5 — Synthetic Data Validation Pipeline for Project Sasha.

Three capabilities:
1. Realism Test     — LLM judges which of two texts (real vs synthetic) was machine-generated.
                      Flags if detection rate >= 85 % (synthetic text is too distinguishable).
2. Annotation Export — Random 100-sentence CSV sample with empty annotation columns for human review.
3. Cohen's Kappa    — Inter-rater agreement calculator from two filled annotation CSVs.

Golden dataset (real Hebrew sentences) is loaded from data/golden_dataset.json.
Provide this file yourself — it is never generated by the pipeline.
Expected schema: [{"text": "...", "source": "...", "labels": [...]}]
"""

from __future__ import annotations

import csv
import json
import logging
import random
from pathlib import Path
from typing import Any

logger = logging.getLogger(__name__)

REALISM_FAIL_THRESHOLD: float = 0.85  # flag if LLM detects synthetic text >= 85 % of the time

_ALL_LABELS: list[str] = [
    "sleep_disturbance",
    "hypervigilance",
    "avoidance",
    "intrusive_memories",
    "anger_irritability",
    "emotional_numbing",
    "guilt_shame",
    "functional_impairment",
]

_REALISM_PROMPT = (
    "You are evaluating two Hebrew text samples. "
    "One was written by a real Israeli person. One was generated by a language model.\n\n"
    "Text A:\n{text_a}\n\n"
    "Text B:\n{text_b}\n\n"
    "Which text was written by a machine? "
    "Answer ONLY with the single letter A or B — nothing else.\n"
    "Answer:"
)


# ---------------------------------------------------------------------------
# Internal helpers
# ---------------------------------------------------------------------------


def _load_golden_dataset() -> list[dict]:
    """Load authentic Hebrew sentences from data/golden_dataset.json (user-provided)."""
    from src.config import GOLDEN_DATASET_PATH

    if not GOLDEN_DATASET_PATH.exists():
        raise FileNotFoundError(
            f"Golden dataset not found at {GOLDEN_DATASET_PATH}.\n"
            "Create data/golden_dataset.json with real (human-written) Hebrew sentences.\n"
            'Expected schema: [{"text": "...", "source": "...", "labels": [...]}]'
        )
    return json.loads(GOLDEN_DATASET_PATH.read_text(encoding="utf-8-sig"))


